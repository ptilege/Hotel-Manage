    var currentIndex = 0;
    var comments = document.querySelectorAll('.comment');

    function showComment(index) {
        comments.forEach(comment => comment.style.display = 'none');
        comments[index].style.display = 'block';
    }

    function prevComment() {
        currentIndex = (currentIndex - 1 + comments.length) % comments.length;
        showComment(currentIndex);
    }

    function nextComment() {
        currentIndex = (currentIndex + 1) % comments.length;
        showComment(currentIndex);
    }

    // Show the first comment initially
    showComment(currentIndex);
</script>